import XCTest
@testable import OpenWeatherMapKit

class WeatherItemCalculationTests: XCTestCase {
    
    func test_convert_to_celsius() {
        let weatherItem = WeatherItem(currentTemp: 236, maxTemp: 247, minTemp: 203, feels_like: 236)
        XCTAssertEqual(weatherItem.celsius.currentTemp, toCelsius(kelvin: weatherItem.kelvin.currentTemp))
        XCTAssertEqual(weatherItem.celsius.maxTemp, toCelsius(kelvin: weatherItem.kelvin.maxTemp))
        XCTAssertEqual(weatherItem.celsius.minTemp, toCelsius(kelvin: weatherItem.kelvin.minTemp))
    }

    func test_convert_to_fahrenheit() {
        let weatherItem = WeatherItem(currentTemp: 236, maxTemp: 247, minTemp: 203, feels_like: 236)
        XCTAssertEqual(weatherItem.fahrenheit.currentTemp, toFahrenheit(celsius: weatherItem.celsius.currentTemp))
        XCTAssertEqual(weatherItem.fahrenheit.maxTemp, toFahrenheit(celsius: weatherItem.celsius.maxTemp))
        XCTAssertEqual(weatherItem.fahrenheit.minTemp, toFahrenheit(celsius: weatherItem.celsius.minTemp))
    }

}
